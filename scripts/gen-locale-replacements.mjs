function buildLocale(code, js, langCurrent, replacements) {
  return {
    htmlLang: code,
    ogLocale: code === "uk" ? "uk_UA" : "en_US",
    localeTag: code === "uk" ? "uk-UA" : "en-US",
    strings: { lang: { current: langCurrent } },
    js,
    replacements: replacements.sort((a, b) => b[0].length - a[0].length),
  };
}

